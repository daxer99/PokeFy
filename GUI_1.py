def classify(file_path):
    global label_packed
    image = Image.open(file_path)
    image = image.resize((120, 120))
    image = numpy.expand_dims(image, axis=0)
    image = numpy.array(image)
    pred = numpy.argmax(model.predict([image])[0])
    sign = classes[pred]
    print(sign)
    label.configure(background='white', foreground='#3689f5', font=('montserrat', 18, 'bold'), text=sign)

def show_classify_button(file_path):
    classify_b = Button(top, text="Identificar Pokemon", command=lambda: classify(file_path), padx=10, pady=5)
    classify_b.configure(background='white', foreground='#3689f5', font=('montserrat', 10, 'bold'))
    classify_b.place(relx=0.79, rely=0.46)

def upload_image():
    try:
        file_path = filedialog.askopenfilename()
        uploaded = Image.open(file_path)
        uploaded.thumbnail(((top.winfo_width() / 2.25), (top.winfo_height() / 2.25)))
        im = ImageTk.PhotoImage(uploaded)

        sign_image.configure(image=im)
        sign_image.image = im
        label.configure(text='')
        show_classify_button(file_path)
    except:
        pass

